

function cambiaBeb(color){
    document.getElementById("bebidas").style.backgroundColor = color;
    document.getElementById("tapas").style.backgroundColor = 'white';
    document.getElementById("pinchos").style.backgroundColor = 'white';
    document.getElementById("raciones").style.backgroundColor = 'white';
    document.getElementById("bocadillos").style.backgroundColor = 'white';
    document.getElementById("postres").style.backgroundColor = 'white';
    
    document.getElementById("art_bebidas").style.display = 'block';
    document.getElementById("art_tapas").style.display = 'none';
    document.getElementById("art_pinchos").style.display = 'none';
    document.getElementById("art_raciones").style.display = 'none';
    document.getElementById("art_bocadillos").style.display = 'none';
    document.getElementById("art_postres").style.display = 'none';
}

